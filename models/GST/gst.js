const mongoose = require('mongoose');
const gstSchema = new mongoose.Schema({
    gstPrice: {
        type: Number,
        required: true,
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
    updatedAt: {
        type: Date,
        default: Date.now,
    },
});
module.exports = mongoose.model('GST', gstSchema);